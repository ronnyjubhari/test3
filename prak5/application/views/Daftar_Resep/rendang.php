			<h2>Rendang Padang</h2>
			<p>Rendang atau randang adalah masakan daging yang berasal dari Minangkabau. Masakan ini
					dihasilkan dari proses memasak suhu rendah dalam waktu lama menggunakan aneka rempah-rempah
					dan santan. Proses memasaknya memakan waktu berjam-jam (biasanya sekitar empat jam)
					hingga yang tinggal hanyalah potongan daging berwarna hitam pekat dan dedak. Dalam suhu
					ruangan, rendang dapat bertahan hingga berminggu-minggu.
			</p>

			<br>
					<h3>Bahan-bahan:</h3>
					<ul>
                        <li>1,5 kg daging sapi</li>
                        <li>2 liter santan yang dibuat dari 2 butir kelapa tua.</li>
					</ul>

                    <br>
					<h3>Bumbu Rempah:</h3>
					<ul>
                        <li>4 lembar daun jeruk purut</li>
                        <li>2 batang serai yang sudah dimemarkan</li>
                        <li>2 lembar daun kunyit yang diikat</li>
                        <li>2 cm asam kandis.</li>
					</ul>

                    <br>
                    <h3>Bumbu Halus:</h3>
                    <ul>
                        <li>100 gram cabe merah besar</li>
                        <li>100 gram cabe merah keriting</li>
                        <li>12 butir bawang merah</li>
                        <li>6 siung bawang putih</li>
                        <li>3 cm kunyit yang sudah dibakar</li>
                        <li>5 butir kemiri</li>
                        <li>2 cm jahe yang sudah dibakar</li>
                        <li>2 cm lengkuas</li>
                        <li>2 sendok teh garam</li>
                        <li>1 sendok teh jintan yang sudah disangrai</li>
                        <li>1/2 sendok makan ketumbar</li>
                        <li>1/2 sendok teh pala</li>
                        <li>Penyedap rasa secukupnya.</li>
                    </ul>

					<br>
					<h3>Cara Memasak:</h3>
					<ol>
						<li>Potong-potong daging sapi yang sudah dibersihkan sesuai selera. Namun jangan terlalu
                            kecil, agar daging tak mudah hancur.</li>
                        <li>Masukkan santan ke dalam wajan besar bersama dengan serai, bawang merah, asam, dan
                            daun kunyit. Masak dan aduk terus sampai santan mendidih, pastikan santan tidak pecah.</li>
                        <li>Setelah mendidih, masukkan bumbu halus dan aduk sesekali lalu masak lagi selama 20-30 menit.</li>
                        <li>Jika santan terlihat sudah berminyak, masukkan potongan daging kemudian masak menggunakan api
                            kecil sampai santan mengental, mengering, dan bumbu meresap ke dalam daging.</li>
                        <li>Masak terus sampai daging menjadi empuk sambil terus diaduk agar bagian bawah tidak gosong.</li>
                        <li>Setelah matang, angkat rendang dan dinginkan.</li>
                        <li>Rendang Padang siap disajikan bersama dengan nasi hangat.</li>
					</ol>

