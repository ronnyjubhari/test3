			<h2>Kerak Telor</h2>
			<p>Ibu kota punya kerak telor khas Betawi sebagai makanan tradisionalnya. Berbahan dasar
				telur bebek atau ayam, yang dicampur dengan beras ketan, serundeng, udang, bawang goreng,
				dan bumbu lain. Kerak telor memiliki rasa gurih dan manis yang menggoyang lidahmu.
			</p>

			<br>
			<h3>Bahan-bahan:</h3>
			<ul>
				<li>Beras ketan putih berkualitas, 100 gram</li>
				<li>250 ml air</li>
				<li>100 gram kelapa parut yang disangrai atau serundeng.</li>
				<li>15 gram ebi atau udang kecil yang direbus, disangrai, kemudian dihaluskan</li>
				<li>5 butir telur bebek atau telur ayam</li>
				<li>30 gram bawang merah goreng kering</li>
				<li>1 sdm minyak goreng</li>
				<li>bawang merah goreng kering sebagai taburan</li>
				<li>garam secukupnya</li>
				<li>gula pasir secukupnya</li>
			</ul>

			<br>
			<h3>Bumbu Halus:</h3>
			<ul>
				<li>4 buah cabai merah keriting</li>
				<li>Â½ sdt merica butir</li>
				<li>3 cm kencur</li>
				<li>1 cm jahe</li>
			</ul>

			<br>
			<h3>Cara Memasak:</h3>
			<ol>
				<li>Beras ketan dibersihkan dengan cara merendamnya selama 12 jam atau satu malam. Jangan buang air rendamannya.</li>
				<li>Cuci kembali beras ketannya sebelum ditiriskan.</li>
				<li>Panaskan penggorengan.</li>
				<li>Kemudian tuangkan minyak dan masukan bumbu yang dihaluskan, tumis hingga baunya harum, sisihkan.</li>
				<li>Siapkan wajan cekung dan panaskan, ambil satu setengaj sendok makan beras ketan dan bubuhkan pada wajan yang telah panas tersebut.</li>
				<li>Siramkan air rendaman beras pada beras tersebut, kurang lebih 3 sendok makan. Biarkan beras hingga setengah kering.</li>
				<li>Siapkan wadah. Ambil 1 butir telur, kemudian kocok.</li>
				<li>Tambahkan pada telur tersebut bumbu yang telah ditumis tadi, ebi, dan bawang goreng masing-masing Â½ sendok teh. Beri juga garam dan gula pasir, masing-
						masing 1/8 sendok teh. Anda bisa memperkirakannya sesuai dengan selera.</li>
				<li>Setelah tercampur rata adonan telur dan bumbu tersebut, siramkan pada wajan yang digunakan untuk mengeringkan beras ketan tadi. Ratakan adonan jangan sampai terlalu tebal.</li>
				<li>Tutup wajan tersebut agar panasnya merata dan kerak telor Anda matang.</li>
				<li>Setelah matang, balik wajan hingga kerak telor tersebut menghadap ke bagian bara api. Lakukan hal ini hingga adonan matang sempurna.</li>
				<li>Angkat dan gunakan sodet atau spatula untuk mengeluarkan kerak telor yang sudah jadi tersebut dari wajannya.</li>
				<li>Taburkan kelapa sangrai dan bawang goreng di atasnya.</li>
			</ol>

