				<h2>Laksa Bogor</h2>
				<p>Laksa adalah makanan berjenis mi yang ditaruh bumbu dengan kebudayaan Peranakan,
					yang digabung dengan elemen Tionghoa dan Melayu. Laksa mempunyai beberapa jenis,
					yang paling dikenal adalah yang berjenis Laksa Penang, bentuk mi-nya bulat putih dan
					sedikit tebal. Di Indonesia juga terdapat beberapa jenis laksa seperti Laksa Bogor dan
					Laksa Betawi.
				</p>

        <br>
				<h3>Bahan-bahan:</h3>
				<ul>
					<li>100 gram kelapa, sangrai hingga kering lalu tumbuk setengah halus</li>
					<li>400 ml kaldu ayam, dari rebusan ayam</li>
					<li>2 lembar daun salam</li>
					<li>2 batang serai, memarkan</li>
					<li>1.500 ml santan, dari 11/2 btr kelapa</li>
					<li>2 batang daun bawang, potong 1 cm</li>
					<li>7 sendok teh garam</li>
					<li>1 sendok teh merica bubuk</li>
					<li>1 sendok teh gula pasir</li>
					<li>3 sendok makan minyak goreng </li>
				</ul>

				<br>
				<h3>Bumbu Halus:</h3>
				<ul>
					<li>8 butir bawang merah</li>
					<li>3 siung bawang putih</li>
					<li>2 buah cabai merah keriting</li>
					<li>3 cm kunyit, bakar</li>
					<li>2 cm jahe</li>
					<li>3 cm lengkuas</li>
					<li>3 sendok teh ketumbar</li>
				</ul>

				<br>
				<h3>Bumbu pelengkap:</h3>
				<ul>
					<li>3 buah paha ayam atas-bawah, rebus, suwir-suwir</li>
					<li>4 butir telur rebus, potong 4 bagian</li>
					<li>200 gram bihun, seduh</li>
					<li>150 gram oncom, potong 2x2 cm, seduh</li>
					<li>10 batang daun kucai, potong 2 cm</li>
				</ul>

				<br>
				<h3>Cara Memasak:</h3>
				<ol>
					<li>Panaskan minyak. Tumis bumbu halus bersama serai dan salam sampai harum.
						Tuang kaldu dan santan. Rebus sampai mendidih.</li>
					<li>Bubuhi garam, merica, gula pasir. Rebus di atas api kecil sampai meresap.
						Tambahkan daun bawang. Aduk rata.</li>
					<li>Masukkan kelapa sangrai. Biarkan sebentar.</li>
					<li>Tata bihun di dalam mangkuk. Taruh suwiran ayam, potongan oncom, potongan kucai, telur rebus.</li>
					<li>Tuang kuah laksa. Sajikan hangat.</li>
				</ol>

