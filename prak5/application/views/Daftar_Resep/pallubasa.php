			<h2>Pallubasa</h2>
			<p>Pallu basa adalah makanan tradisional Makassar, Sulawesi Selatan. Seperti Coto Mangkasara (Coto Makassar),
				Pallubasa juga terbuat dari jeroan (isi dalam perut) sapi atau kerbau. Namun kini kuliner ini telah berkembang menjadi
				pallu basa ayam dan pallu basa kepala ikan. Proses memasaknya pun hampir sama dengan Coto Makassar yang
				lebih terkenal, yakni jeroan direbus dalam waktu lama.
			</p>

			<br>
			<h3>Bahan-bahan:</h3>
			<ul>
				<li>Daging sapi - 250 gram</li>
				<li>Kaldu sapi - 600 ml</li>
				<li>Kelapa parut, sangrai lalu haluskan hingga berminyak - 30 gram</li>
				<li>Serai, memarkan - 1 batang</li>
				<li>Lengkuas, memarkan - 2 cm</li>
				<li>Gula merah - 1 sdt</li>
				<li>Kayu manis - 1 cm</li>
				<li>Cengkeh - 4 buah</li>
				<li>Air asam - 30 ml</li>
				<li>Garam - 1 sdt</li>
				<li>Penyedap rasa (opsional) - 1 sdt</li>
				<li>Minyak, untuk menumis - 3 sdm</li>
			</ul>

			<br>
			<h3>Bumbu Halus:</h3>
			<ul>
				<li>Bawang putih - 5 siung</li>
				<li>Bawang merah - 8 butir</li>
				<li>Cabai keriting - 5 buah</li>
				<li>Jahe - 2 cm</li>
				<li>Pala - 1/2 biji</li>
				<li>Ketumbar - 1 sdt</li>
				<li>Jinten - 1/2 sdt</li>
				<li>Pelengkap:</li>
				<li>Irisan daun bawang - secukupnya</li>
				<li>Bawang goreng - secukupnya</li>
				<li>Irisan jeruk nipis - secukupnya</li>
				<li>Sambal - secukupnya</li>
				<li>Kecap â€“ secukupnya</li>
			</ul>

			<br>
			<h3>Cara Membuat:</h3>
			<ol>
				<li>Rebus daging sapi hingga empuk. Angkat dan dinginkan, lalu potong-potong kotak. Sisihkan 600 ml air sisa
					rebusan daging sebagai kaldu sapi.</li>
				<li>Panaskan minyak. Tumis bumbu halus bersama dengan gula merah, serai, lengkuas, kayu manis, cengkeh
					hingga harum dan matang.</li>
				<li>Tuangkan air kaldu sapi. Aduk rata lalu masak dengan api kecil hingga mendidih.</li>
				<li>Masukkan air asam, garam, penyedap rasa, dan kelapa sangrai. Aduk rata dan masak hingga bumbu meresap
					dan kuah mengental. Angkat.</li>
				<li>Siap disajikan.</li>
			</ol>

			<br>
			<h3>Tips:</h3>
			<ol>
				<li>Jika suka, anda bisa menggunakan jeroan sapi sesuai selera.</li>
				<li>Daging bisa dimasak menggunakan panci presto agar lebih cepat empuk.</li>
				<li>Pallu basa biasa disajikan dengan kuning telur ayam mentah.</li>
			</ol>

