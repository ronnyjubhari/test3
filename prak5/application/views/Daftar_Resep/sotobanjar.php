					<h2>Soto Banjar</h2>
					<p>Soto Banjar adalah soto khas suku Banjar, Kalimantan Selatan dengan bahan utama ayam serta
                        memiliki aroma harum rempah-rempah seperti kayu manis, biji pala, dan cengkih. Adakalanya
                        pembuatan kuah soto banjar dapat dicampurkan dengan sedikit susu yang membuat warna kuahnya
                        mejadi tidak bening, tetapi sedikit keruh. Soto ini berisi daging ayam yang sudah
                        disuwir-suwir, dengan tambahan perkedel, kentang rebus, rebusan telur, potongan wortel
                        dan ketupat.
                    </p>

					<br>
					<h3>Bahan-bahan:</h3>
					<ul>
                        <li>Siapkan beberapa potong ayam kampung</li>
                        <li>1,5 liter kaldu ayam</li>
                        <li>250 ml susu cair</li>
                        <li>Minyak goreng secukupnya untuk menumis.</li>
					</ul>

                    <br>
                    <h3>Bumbu Halus:</h3>
                    <ul>
                        <li>5 siung bawang putih</li>
                        <li>8 butir bawang merah</li>
                        <li>1 sendok teh lada</li>
                        <li>3 butir kapulaga yang sudah disangrai</li>
                        <li>1/2 sendok teh pala bubuk</li>
                        <li>Garam secukupnya.</li>
                    </ul>

                    <br>
                    <h3>Bahan lainnya:</h3>
                    <ul>
                        <li>3 batang daun bawang</li>
                        <li>4 buah cengkeh</li>
                        <li>10 cm kayu manis yang dipotong menjadi 2 bagian</li>
                        <li>2 batang seledri.</li>
                    </ul>

					<br>
					<h3>Cara Memasak:</h3>
					<ol>
                        <li>Cuci ayam dan potong menjadi beberapa bagian.</li>
                        <li>Rebus ayam menggunakan air sebanyak tiga liter dengan api sedang.</li>
                        <li>Sambil menunggu ayam matang, haluskan bumbu terlebih dahulu.</li>
                        <li>Setelah halus, tumis sampai wanginya tercium.</li>
                        <li>Kalau daging ayam sudah matang, angkat dan suwir secukupnya.
                            Kaldu bekas rebusan ayam jangan dibuang karena akan digunakan untuk kuah soto.</li>
                        <li>Rebus kembali kaldu atau air rebusan ayam, lalu masukkan bumbu halus yang sudah ditumis.</li>
                        <li>Masukkan susu cair, daun bawang, kayu manis, dan cengkeh. Tambahkan juga garam secukupnya.</li>
                        <li>Cicipi kuah sotonya, jika dirasa kurang, kamu bisa menambah garam atau penyedap rasa.</li>
                        <li>Kuah soto sudah siap, sisihkan.</li>
                    </ol>

                    <br>
                    <h3>Penyajian:</h3>
                    <ol>
                        <li>Iris seledri.</li>
                        <li>Siram 100 gram soun dengan air panas.</li>
                        <li>Rebus telur ayam dan potong menjadi 6 bagian, atau kalau kekecilan potong sesuai selera.</li>
                        <li>Siapkan perkedel secukupnya.</li>
                        <li>Buat sambal soto menggunakan cabe rawit.</li>
                        <li>Beberapa potong ketupat atau nasi putih.</li>
                        <li>Jeruk nipis.</li>
                        <li>Kecap manis.</li>
                        <li>Siapkan mangkok, masukkan soun, ketupat, suwiran ayam, lalu siram dengan kuah secukupnya.</li>
                        <li>Tambahkan perasan jeruk nipis, kecap manis, lalu taburi dengan selederi.</li>
                    </ol>

