			<h2>Rawon Surabaya</h2>
			<p>Rawon adalah masakan Indonesia berupa sup daging berkuah hitam dengan campuran bumbu khas
				yang menggunakan kluwek. Rawon, meskipun dikenal sebagai masakan khas Jawa Timur (daerah
				Arekan), dikenal pula oleh masyarakat Jawa Tengah sebelah timur (daerah Surakarta).
			</p>

			<br>
			<h3>Bahan-bahan:</h3>
			<ul>
					<li>500 gram daging sapi agak berlemak</li>
					<li>2 liter air</li>
					<li>3 batang serai, memarkan</li>
					<li>3 cm lengkuas, memarkan</li>
					<li>5 lembar daun jeruk</li>
					<li>2 lembar daun salam</li>
					<li>4 sdm minyak untuk menumis</li>
					<li>2 tangkai daun bawang, potong-potong</li>
			</ul>



                    <br>
                    <h3>Bumbu Halus:</h3>
                    <ul>
						<li>5 butir bawang merah</li>
						<li>3 siung bawang putih</li>
						<li>2 butir kemiri</li>
						<li>1 sdt ketumbar</li>
						<li>2 cm jahe</li>
						<li>2 cm kunyit</li>
						<li>3 bh cabai merah (opsional)</li>
						<li>4 bh keluwak tua, kukus, ambil isinya</li>
						<li>2 sdt garam</li>
                    </ul>

                    <br>
                    <h3>Pelengkap:</h3>
                    <ul>
						<li>Kerupuk udang</li>
						<li>100 gram tauge pendek</li>
						<li>4 butir telur asin</li>
						<li>Sambal</li>
                    </ul>

					<br>
					<h3>Cara Memasak:</h3>
					<ol>
						<li>Masak daging dengan api kecil</li>
						<li>Masukkan daun salam, serai, lengkuas, dan daun jeruk sampai daging empuk dan matang.</li>
						<li>Angkat daging, lalu potong kecil-kecil.</li>
						<li>Saring rebusan air daging, sisihkan bersama irisan daging.</li>
						<li>Haluskan semua bumbu halus, tumis sampai berbau harum.</li>
						<li>Angkat, lalu masukkan ke dalam kaldu daging.</li>
						<li>Masak dengan api kecil sampai mendidih kembali.</li>
						<li>Cicipi, apakah rasanya sudah pas.</li>
						<li>Masukkan daun bawang yang sudah dipotong, didihkan sebentar.</li>
						<li>Tuang rawon kedalam mangkuk, taburkan tauge pendek di atasnya.</li>
						<li>Sajikan bersama nasi hangat, telur asin dan sambal.</li>
					</ol>

