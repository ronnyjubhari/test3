					<h2>Sop Konro</h2>
					<p>Sop Konro adalah masakan sup iga sapi khas Indonesia yang berasal dari tradisi Bugis
                        dan Makassar. Sop ini biasanya dibuat dengan bahan iga sapi atau daging sapi. Masakan
                        berkuah warna coklat kehitaman ini biasa dimakan dengan burasa dan ketupat yang
                        dipotong-potong terlebih dahulu. Rasa pedas dan berbumbu ini dibuat dari campuran rempah-rempah,
                        seperti ketumbar, keluwak (buah yang menyebabkan masakan berwarna hitam), sedikit pala,
                        kunyit, kencur, kayu manis, asam, daun lemon, cengkih, dan daun salam.
                    </p>

					<br>
					<h3>Bahan-bahan:</h3>
					<ul>
                        <li>1 kg iga sapi.</li>
                        <li>1700 ml air.</li>
                        <li>1/2 butir kelapa parut kemudian disangrai hingga kering lalu dihaluskan.</li>
                        <li>1 lembar daun salam.</li>
                        <li>2 lembar daun jeruk.</li>
                        <li>2 cm kayu manis.</li>
                        <li>Daun bawang dan Daun Seledri secukupnya.</li>
                        <li>Minyak secukupnya untuk menumis.</li>
                        <li>Gula pasir,garam,kaldu sapi bubuk secukupnya.</li>
					</ul>

                    <br>
                    <h3>Bumbu Halus:</h3>
                    <ul>
                        <li>10 siung bawang merah.</li>
                        <li>6 siung bawang putih.</li>
                        <li>4 butir kemiri.</li>
                        <li>1 buah kluwak.</li>
                        <li>5 batang sereh.</li>
                        <li>Serimpang lengkuas.</li>
                        <li>2 cm kunyit.</li>
                        <li>1/2 sdt merica bubuk.</li>
                        <li>1 sdt jinten bubuk.</li>
                        <li>1 sdt ketumbar bubuk.</li>
                        <li>1/4 pala bubuk.</li>
                    </ul>

                    <br>
                    <h3>Bumbu pelengkap:</h3>
                    <ul>
                        <li>Nasi putih atau buras.</li>
                        <li>Bawang goreng.</li>
                        <li>Sambel & jeruk nipis.</li>
                    </ul>

					<br>
					<h3>Cara Memasak:</h3>
					<ol>
                        <li>Presto iga sapi hingga empuk dengan air secukupnya,saring ambil kaldunya 1 liter.</li>
                        <li>Pindahkan kepanci biasa,masukan iga dan kaldunya lalu tambah air biasa 700 ml atau secukupnya.</li>
                        <li>Tumis bumbu halus dengan minyak secukupnya hingga wangi, lalu masukan kedalam panci
                            iga, masukkan juga daun salam, daun jeruk, kayu manis dan kelapa sangrai yang
                            dihaluskan, aduk rata, beri gula garam dan kaldu sapi bubuk, didihkan cek rasa.</li>
                        <li>Masukan potongan daun seledri dan daun bawang secukupnya didihkan sebentar matikan api.</li>
                        <li>Sajikan dimangkok saji lalu taburi bawang goreng.</li>
					</ol>

