						<h2>Soto Padang</h2>
					<p>Soto Padang adalah hidangan berkuah kaldu sapi dengan bahan irisan daging sapi yang sudah
                        digoreng kering, bihun (mie dari tepung beras), ditambah perkedel kentang dan dihidangkan
                        panas-panas. Selain di Kota Padang, Sumatra Barat, hidangan Soto Padang juga mudah ditemukan
                        di berbagai restoran Padang di penjuru dunia.
                    </p>

					<br>
					<h3>Bahan-bahan:</h3>
					<ul>
                        <li>500 gram daging sapi (campur dengan tulang supaya kaldunya lebih kerasa).</li>
                        <li>2 liter air.</li>
                        <li>minyak goreng untuk menumis.</li>
					</ul>

                    <br>
					<h3>Bumbu Cemplung:</h3>
					<ul>
                        <li>2 batang sereh digeprek.</li>
                        <li>3 lembar daun salam.</li>
                        <li>4 lembar daun jeruk.</li>
                        <li>5 buah kapulaga.</li>
                        <li>8 kelopak bunga lawang.</li>
                        <li>5 buah cengkeh.</li>
                        <li>4 cm kayu manis.</li>
                        <li>2 batang daun bawang diiris.</li>
					</ul>

                    <br>
                    <h3>Bumbu Halus:</h3>
                    <ul>
                        <li>10 siung bawang putih.</li>
                        <li>6 buah bawang merah.</li>
                        <li>3 ruas lengkuas.</li>
                        <li>2 ruas jahe.</li>
                        <li>1 ruas kunyit.</li>
                        <li>1 sdt pala bubuk.</li>
                        <li>1 sdm ketumbar bubuk.</li>
                        <li>2 sdt lada bubuk.</li>
                        <li>garam secukupnya.</li>
                    </ul>

                    <br>
                    <h3>Bumbu pelengkap:</h3>
                    <ul>
                        <li>sohun siram dengan air panas, rendam sebentar, angkat dan tiriskan.</li>
                        <li>perkedel kentang.</li>
                        <li>kerupuk merah.</li>
                        <li>sambal.</li>
                        <li>seledri diiris halus.</li>
                        <li>bawang goreng.</li>
                        <li>kecap.</li>
                        <li>cuka.</li>
                    </ul>

					<br>
					<h3>Cara Memasak:</h3>
					<ol>
						<li>Rebus daging sampai mendidih keluar buihnya, buah buih yang mengapung, masukkan
                            semua bahan cemplung, lanjutkan merebus sampai daging empuk, masukan daun bawang,
                            tunggu layu, matikan kompor, angkat daging, dan tunggu dingin.</li>
                        <li>Panaskan minyak goreng, tumis bumbu halus sampai wangi, campur bumbu yang sudah ditumis
                            ke dalam air rebusan daging, masak sebentar sampai bumbu dan kaldu menyatu. koreksi rasa.</li>
                        <li>Iris tipis daging yang sudah direbus, kemudian goreng sampai garing.</li>
                        <li>Tata sohun, daging didalam mangkok, siram dengan kuah soto, beri kerupuk, perkedel, seledri dan sambal.</li>
                        <li>Sajikan.</li>
					</ol>

